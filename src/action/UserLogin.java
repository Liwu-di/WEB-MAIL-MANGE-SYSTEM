package action;

public class UserLogin {
	private String username;
	private String password;
	//setter getter
	public String getUsername() {
		return username;
	}
	public void setUsername(String uname) {
		this.username=uname;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String pass) {
		this.password=pass;
	}
}
